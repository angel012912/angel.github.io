// Function to get the user's language preference from a cookie or session variable
function getUserLanguage() {
    let preferedLanguage = localStorage.getItem('lang');
    if (preferedLanguage) {
        return preferedLanguage;
    }
    return 'en';
}

function setPreferedLanguage() {
    if (localStorage.getItem('lang') == 'es'){
        localStorage.setItem('lang', 'en');
        translate();
        return;
    }
    else if (localStorage.getItem('lang') == 'en'){    
        localStorage.setItem('lang', 'es');
        translate();
        return;
    }
}

function translate(){
    // Set the language on page load
    const userLanguage = getUserLanguage();
    translateElements(userLanguage);
    return userLanguage;
}

function initializeLanguage(){
    const userLanguage = translate();
    if (userLanguage == "en"){
        document.getElementById('language-input').click();
    }
    document.getElementById('language-input').addEventListener('click', setPreferedLanguage);
}

initializeLanguage();